import { useState, useEffect } from 'react';
import { Clock, Package, User, Trash2, X, Loader2 } from 'lucide-react';
import { holdApi } from '../../lib/api';
import { useSettings } from '../../contexts/SettingsContext';

interface HeldOrder {
    id: string;
    holdNumber: string;
    customerName?: string | null;
    totalAmount: number;
    itemCount: number;
    createdAt: string;
    expiresAt?: string | null;
    holdReason?: string | null;
}

interface ResumeHoldDialogProps {
    isOpen: boolean;
    onClose: () => void;
    onResume: (holdId: string) => void;
}

/**
 * Resume Hold Dialog
 * Shows list of held carts and allows resuming them
 */
export function ResumeHoldDialog({ isOpen, onClose, onResume }: ResumeHoldDialogProps) {
    const { settings } = useSettings();
    const cs = settings.currencySymbol;
    const [holds, setHolds] = useState<HeldOrder[]>([]);
    const [loading, setLoading] = useState(false);
    const [deleting, setDeleting] = useState<string | null>(null);

    // Load holds when dialog opens
    useEffect(() => {
        if (isOpen) {
            loadHolds();
        }
    }, [isOpen]);

    const loadHolds = async () => {
        setLoading(true);
        try {
            const response = await holdApi.list();
            if (response.data?.success) {
                setHolds(response.data?.data || []);
            }
        } catch (error: any) {
            console.error('Failed to load holds:', error);
        } finally {
            setLoading(false);
        }
    };

    const handleResume = (holdId: string) => {
        onResume(holdId);
    };

    const handleDelete = async (holdId: string, holdNumber: string, e: React.MouseEvent) => {
        e.stopPropagation();

        if (!confirm(`Delete hold ${holdNumber}? This cannot be undone.`)) {
            return;
        }

        setDeleting(holdId);
        try {
            const response = await holdApi.delete(holdId);
            if (response.data?.success) {
                setHolds((prev) => prev.filter((h) => h.id !== holdId));
            }
        } catch (error: any) {
            console.error('Failed to delete hold:', error);
        } finally {
            setDeleting(null);
        }
    };

    const formatDateTime = (dateString: string) => {
        const date = new Date(dateString);
        return date.toLocaleDateString('en-US', { 
            month: 'short', 
            day: 'numeric',
            hour: 'numeric',
            minute: '2-digit',
            hour12: true
        });
    };

    if (!isOpen) return null;

    return (
        <div className="fixed inset-0 bg-black bg-opacity-50 flex items-center justify-center z-50" onClick={onClose}>
            <div 
                className="bg-white rounded-lg shadow-xl w-full max-w-2xl max-h-[80vh] overflow-hidden flex flex-col" 
                onClick={(e) => e.stopPropagation()}
            >
                {/* Header */}
                <div className="flex items-center justify-between px-6 py-4 border-b">
                    <h2 className="text-xl font-semibold text-gray-900 flex items-center gap-2">
                        <Package className="h-5 w-5 text-orange-500" />
                        Resume Held Cart
                    </h2>
                    <button
                        onClick={onClose}
                        className="text-gray-400 hover:text-gray-600 transition-colors"
                        aria-label="Close"
                    >
                        <X className="w-5 h-5" />
                    </button>
                </div>

                {/* Content */}
                <div className="flex-1 overflow-y-auto p-6">
                    {loading && (
                        <div className="py-12 text-center text-gray-500 flex flex-col items-center">
                            <Loader2 className="h-8 w-8 animate-spin text-orange-500 mb-3" />
                            Loading held orders...
                        </div>
                    )}

                    {!loading && holds.length === 0 && (
                        <div className="py-12 text-center">
                            <Package className="h-12 w-12 text-gray-300 mx-auto mb-3" />
                            <p className="text-gray-500">No held orders found</p>
                            <p className="text-xs text-gray-400 mt-1">
                                Put a cart on hold to save it for later
                            </p>
                        </div>
                    )}

                    {!loading && holds.length > 0 && (
                        <div className="space-y-3">
                            {holds.map((hold) => (
                                <div
                                    key={hold.id}
                                    className="border rounded-lg p-4 hover:bg-gray-50 cursor-pointer transition-colors"
                                    onClick={() => handleResume(hold.id)}
                                >
                                    <div className="flex items-start justify-between mb-2">
                                        <div className="flex-1">
                                            <div className="flex items-center gap-2 mb-1">
                                                <span className="font-semibold text-sm">{hold.holdNumber}</span>
                                                {hold.customerName && (
                                                    <span className="inline-flex items-center px-2 py-0.5 rounded border border-gray-300 text-xs font-medium text-gray-700 bg-white">
                                                        <User className="h-3 w-3 mr-1" />
                                                        {hold.customerName}
                                                    </span>
                                                )}
                                            </div>

                                            {hold.holdReason && (
                                                <p className="text-xs text-gray-600 mb-2">{hold.holdReason}</p>
                                            )}
                                        </div>

                                        <button
                                            className="p-2 text-red-500 hover:text-red-600 hover:bg-red-50 rounded transition-colors disabled:opacity-50"
                                            onClick={(e) => handleDelete(hold.id, hold.holdNumber, e)}
                                            disabled={deleting === hold.id}
                                            aria-label="Delete hold"
                                        >
                                            {deleting === hold.id ? (
                                                <Loader2 className="h-4 w-4 animate-spin" />
                                            ) : (
                                                <Trash2 className="h-4 w-4" />
                                            )}
                                        </button>
                                    </div>

                                    <div className="grid grid-cols-3 gap-4 text-sm">
                                        <div>
                                            <span className="text-gray-500 text-xs">Items</span>
                                            <p className="font-medium">{hold.itemCount}</p>
                                        </div>
                                        <div>
                                            <span className="text-gray-500 text-xs">Total</span>
                                            <p className="font-medium">{cs} {hold.totalAmount.toLocaleString()}</p>
                                        </div>
                                        <div>
                                            <span className="text-gray-500 text-xs">Held</span>
                                            <p className="font-medium text-xs">
                                                {formatDateTime(hold.createdAt)}
                                            </p>
                                        </div>
                                    </div>

                                    {hold.expiresAt && (
                                        <div className="mt-2 flex items-center gap-1 text-xs text-orange-600">
                                            <Clock className="h-3 w-3" />
                                            <span>
                                                Expires {formatDateTime(hold.expiresAt)}
                                            </span>
                                        </div>
                                    )}
                                </div>
                            ))}
                        </div>
                    )}
                </div>

                {/* Footer */}
                <div className="px-6 py-4 border-t">
                    <button 
                        onClick={onClose}
                        className="w-full py-2 px-4 border border-gray-300 rounded text-gray-700 hover:bg-gray-50 transition-colors"
                    >
                        Close
                    </button>
                </div>
            </div>
        </div>
    );
}

export default ResumeHoldDialog;
